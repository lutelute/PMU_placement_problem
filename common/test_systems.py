def get_system(name: str) -> dict:
    """Retrieve a test system by name.

    Parameters
    ----------
    name : str
        One of the keys in ALL_SYSTEMS.

    Returns
    -------
    dict
        System data dictionary.

    Raises
    ------
    KeyError
        If the name is not recognized.
    """
    if name not in ALL_SYSTEMS:
        raise KeyError(
            f"Unknown system '{name}'. "
            f"Available: {list(ALL_SYSTEMS.keys())}"
        )
    return ALL_SYSTEMS[name]
